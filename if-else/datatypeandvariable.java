public class datatypeandvariable {
	public static void main(String[] args) {
	
	   String name ="Abhishek";
	   int age = 20;
	   char gender = 'm';
	   double weight = 60.5;
	   boolean aadhar = true;
	   
	   
	   System.out.println("name  "+  name);	
	   System.out.println("age  "+  age);
	   System.out.println("char  "+  gender);	
	   System.out.println("double  "+  weight);
	   System.out.println("boolean " +  aadhar);
	   }
}
