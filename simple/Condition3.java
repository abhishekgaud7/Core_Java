package com.simple;

public class Condition3 
{
	public static void main(String[] args) 
	{
		int a=-10;
		
		if(a>=0)
		{
			if(a==0)
			{
				System.out.println("Your No is Zero...."+a);
			}
		
			else
			{
				System.out.println("Your No is Positive...."+a);
			}
		}
		else
		{
			System.out.println("Your No is Negative...."+a);
		}
	}
}
